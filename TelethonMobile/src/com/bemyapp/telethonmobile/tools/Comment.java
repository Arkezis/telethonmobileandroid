package com.bemyapp.telethonmobile.tools;

public class Comment {
	private long note;
	private String comment;

	public void setNote(long note) {
		this.note = note;
	}

	public long getNote() {
		return note;
	}

	public void setComment(String comment) {
		this.comment = comment;
	}

	public String getComment() {
		return comment;
	}

}
